-- Helper function
CREATE OR REPLACE FUNCTION public.has_role(role_name text)
RETURNS boolean AS $$
BEGIN
  RETURN EXISTS (SELECT 1 FROM public.users u WHERE u.id = auth.uid() AND u.role = role_name);
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

