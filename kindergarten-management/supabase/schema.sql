-- ===============
-- 1) Core tables
-- ===============

-- Users profile
CREATE TABLE IF NOT EXISTS public.users (
  id uuid PRIMARY KEY REFERENCES auth.users(id) ON DELETE CASCADE,
  full_name text NOT NULL,
  email text,
  phone text,
  role text NOT NULL DEFAULT 'Parent' CHECK (role IN ('Admin','Teacher','Accountant','Parent')),
  teacher_code text UNIQUE,
  avatar text,
  created_at timestamptz NOT NULL DEFAULT NOW(),
  updated_at timestamptz NOT NULL DEFAULT NOW()
);

-- Grades
CREATE TABLE IF NOT EXISTS public.grades (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name text NOT NULL UNIQUE CHECK (name IN ('Mầm','Chồi','Lá')),
  sort_order int NOT NULL DEFAULT 0,
  description text,
  created_at timestamptz NOT NULL DEFAULT NOW(),
  updated_at timestamptz NOT NULL DEFAULT NOW()
);

-- Classes
CREATE TABLE IF NOT EXISTS public.classes (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  class_code text UNIQUE,
  grade_id bigint REFERENCES public.grades(id) ON DELETE SET NULL,
  name text NOT NULL,
  teacher_id uuid REFERENCES public.users(id) ON DELETE SET NULL,
  room text,
  max_students int NOT NULL DEFAULT 30,
  description text,
  created_at timestamptz NOT NULL DEFAULT NOW(),
  updated_at timestamptz NOT NULL DEFAULT NOW(),
  UNIQUE (room, name)
);

-- Students
CREATE TABLE IF NOT EXISTS public.students (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  class_id bigint NOT NULL REFERENCES public.classes(id) ON DELETE CASCADE,
  student_code text NOT NULL UNIQUE,
  full_name text NOT NULL,
  date_of_birth date,
  gender text CHECK (gender IN ('Male','Female')),
  ethnicity text,
  nationality text DEFAULT 'Việt Nam',
  address text,
  enrolled_date date,
  health_info jsonb NOT NULL DEFAULT '{}',
  avatar text,
  created_at timestamptz NOT NULL DEFAULT NOW(),
  updated_at timestamptz NOT NULL DEFAULT NOW()
);

-- Parents
CREATE TABLE IF NOT EXISTS public.parents (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  user_id uuid REFERENCES public.users(id) ON DELETE SET NULL,
  full_name text NOT NULL,
  phone text NOT NULL,
  email text,
  relationship text NOT NULL DEFAULT 'Guardian' CHECK (relationship IN ('Father','Mother','Guardian')),
  occupation text,
  address text,
  created_at timestamptz NOT NULL DEFAULT NOW(),
  updated_at timestamptz NOT NULL DEFAULT NOW()
);

-- Student-Parent link
CREATE TABLE IF NOT EXISTS public.student_parent (
  student_id uuid NOT NULL REFERENCES public.students(id) ON DELETE CASCADE,
  parent_id uuid NOT NULL REFERENCES public.parents(id) ON DELETE CASCADE,
  relation_type text NOT NULL DEFAULT 'Guardian',
  is_primary boolean NOT NULL DEFAULT false,
  created_at timestamptz NOT NULL DEFAULT NOW(),
  PRIMARY KEY (student_id, parent_id),
  UNIQUE (student_id, parent_id)
);

-- Attendance (CHÚ Ý: dùng `attendance_date` không phải `date`)
CREATE TABLE IF NOT EXISTS public.attendance (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  student_id uuid NOT NULL REFERENCES public.students(id) ON DELETE CASCADE,
  class_id bigint NOT NULL REFERENCES public.classes(id) ON DELETE CASCADE,
  attendance_date date NOT NULL,
  status text NOT NULL CHECK (status IN ('present','absent','late')),
  check_in_time time,
  check_out_time time,
  note text,
  created_by uuid REFERENCES public.users(id) ON DELETE SET NULL,
  created_at timestamptz NOT NULL DEFAULT NOW(),
  updated_at timestamptz NOT NULL DEFAULT NOW(),
  UNIQUE (student_id, attendance_date)
);

-- Fee types
CREATE TABLE IF NOT EXISTS public.fee_types (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name text NOT NULL UNIQUE,
  amount_vnd numeric(12,0) NOT NULL CHECK (amount_vnd > 0),
  grade_id bigint REFERENCES public.grades(id) ON DELETE SET NULL,
  description text,
  is_active boolean NOT NULL DEFAULT true,
  created_at timestamptz NOT NULL DEFAULT NOW(),
  updated_at timestamptz NOT NULL DEFAULT NOW()
);

-- Fee records
CREATE TABLE IF NOT EXISTS public.fee_records (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  student_id uuid NOT NULL REFERENCES public.students(id) ON DELETE CASCADE,
  class_id bigint NOT NULL REFERENCES public.classes(id) ON DELETE CASCADE,
  fee_type_id bigint NOT NULL REFERENCES public.fee_types(id) ON DELETE RESTRICT,
  school_year text NOT NULL,
  month int CHECK (month >= 1 AND month <= 12),
  amount_vnd numeric(12,0) NOT NULL CHECK (amount_vnd > 0),
  paid_amount_vnd numeric(12,0) NOT NULL DEFAULT 0 CHECK (paid_amount_vnd >= 0),
  paid_date date,
  due_date date,
  payment_method text CHECK (payment_method IN ('cash','bank_transfer')),
  status text NOT NULL DEFAULT 'unpaid' CHECK (status IN ('unpaid','partial','paid')),
  created_by uuid REFERENCES public.users(id) ON DELETE SET NULL,
  created_at timestamptz NOT NULL DEFAULT NOW(),
  updated_at timestamptz NOT NULL DEFAULT NOW(),
  UNIQUE (student_id, fee_type_id, school_year, month)
);

-- Notifications
CREATE TABLE IF NOT EXISTS public.notifications (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  recipient_parent_id uuid REFERENCES public.parents(id) ON DELETE CASCADE,
  recipient_user_id uuid REFERENCES public.users(id) ON DELETE CASCADE,
  title text NOT NULL,
  body text NOT NULL,
  kind text NOT NULL DEFAULT 'general' CHECK (kind IN ('general','event','holiday','request','absence')),
  target_type text DEFAULT 'specific',
  student_id uuid REFERENCES public.students(id) ON DELETE SET NULL,
  attendance_id uuid REFERENCES public.attendance(id) ON DELETE SET NULL,
  sent_by uuid REFERENCES public.users(id) ON DELETE SET NULL,
  sent_at timestamptz,
  created_at timestamptz NOT NULL DEFAULT NOW(),
  updated_at timestamptz NOT NULL DEFAULT NOW()
);

-- Notification reads
CREATE TABLE IF NOT EXISTS public.notification_reads (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  notification_id uuid NOT NULL REFERENCES public.notifications(id) ON DELETE CASCADE,
  parent_id uuid REFERENCES public.parents(id) ON DELETE CASCADE,
  user_id uuid REFERENCES public.users(id) ON DELETE CASCADE,
  read_at timestamptz NOT NULL DEFAULT NOW(),
  UNIQUE (notification_id, parent_id, user_id)
);

-- School settings
CREATE TABLE IF NOT EXISTS public.school_settings (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  school_name text NOT NULL,
  address text,
  phone text,
  email text,
  school_year text NOT NULL,
  academic_year_start date,
  academic_year_end date,
  logo_url text,
  created_at timestamptz NOT NULL DEFAULT NOW(),
  updated_at timestamptz NOT NULL DEFAULT NOW(),
  UNIQUE (school_year)
);

